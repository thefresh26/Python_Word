#Propiedades

class empleado:
    def __init__(self, cliente, compra):
        self.cliente = cliente
        self.compra = compra
        
    def __getcliente(self):
        return self.__cliente
    def __getcompra(self):
        return self.__compra
    
    def __setcliente(self):
        return self.__cliente
    def __setcompra(self):
        return self.__compra
    
    def __delcliente(self):
        return self.__cliente
    def __delcompra(self):
        return self.__compra
    
        